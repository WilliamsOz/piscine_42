#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int main(void)
{
	char i;

	i = 122;
	while (i >= 97)
	{
		ft_putchar(i);
		i--;
		ft_putchar(i - 32);
		i--;
	}
	return (0);
}
