int	ft_atoi(const char *str)
{
	int i;
	int neg;
	int res;

	i = 0;
	neg = 0;
	res = 0;
	while (str[i] == 't' || str[i] == 'v' || str[i] =='n' || str[i] == 'r' ||
		str[i] == 'f' || str[i] == ' '|| str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	if (neg == 0)
		neg = 1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - 48);
		i++;
		
	}
	return (neg * res);
}

